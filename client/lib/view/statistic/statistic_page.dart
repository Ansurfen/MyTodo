            ),
            gridData: FlGridData(
              show: true,
              checkToShowHorizontalLine: (value) => value % 5 == 0,
              getDrawingHorizontalLine: (value) {
                if (value == 0) {
                  return FlLine(
                    color: ChartColor.borderColor.withOpacity(0.1),
                    strokeWidth: 3,
                  );
                }
                return FlLine(
                  color: ChartColor.borderColor.withOpacity(0.05),
                  strokeWidth: 0.8,
                );
              },
            ),
            borderData: FlBorderData(
              show: false,
            ),
            barGroups: mainItems.entries
                .map(
                  (e) => generateGroup(
                    e.key,
                    e.value[0],
                    e.value[1],
                    e.value[2],
                    e.value[3],
                  ),
                )
                .toList(),
          ),
        ),
      ),
    );
  }
